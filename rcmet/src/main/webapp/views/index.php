<div class="span-7 last">
	<h5>Background Information</h5>
	<p>
	The Regional Climate Model Evaluation Toolkit is comprised of 
	scripts and interfaces that make it easy to interact with the observational
	data provided by the Regional Climate Model Evaluation Database for the 
	purpose of conducting model-to-data intercomparison and validation.
	</p>
	<h5>Getting Help</h5>
	<p>
	The Regional Climate Model Evaluation System is actively being developed
	at the NASA Jet Propulsion Laboratory (JPL), in collaboration with the UCLA Joint
	Institute for Regional Earth System Science and Engineering (JIFRESSE). The 
	latest information is always available on the <a href="#">RCMES Website</a>.
